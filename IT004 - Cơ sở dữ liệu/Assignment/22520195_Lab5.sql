--5. Lương của nhân viên phải không được cao hơn lương của người quản lý trực tiếp của mình.
---			INSERT	DELETE	UPDATE
---NHANVIEN    +	  -		  +(LUONG, MA_NQL)
CREATE TRIGGER luongnv_luongnql ON NHANVIEN
FOR INSERT, UPDATE
AS
BEGIN
	IF EXISTS (SELECT *
			   FROM INSERTED 
			   WHERE INSERTED.LUONG > (SELECT LUONG 
									   FROM NHANVIEN NV JOIN INSERTED INS ON NV.MANV = INS.MA_NQL
									   )
			  )
	BEGIN
		PRINT 'LOI: LUONG NHAN VIEN - LUONG NQL KHONG HOP LE'
		ROLLBACK TRANSACTION
	END
	ELSE
	BEGIN
		PRINT 'THEM/SUA THANH CONG'
	END
END
GO
--6. Không được phân công thêm đề án cho nhân viên đang làm từ 02 đề án trở lên.
---			INSERT	DELETE	UPDATE
---PHANCONG    +	  -		  -
CREATE TRIGGER phancongdean ON PHANCONG
FOR INSERT
AS
BEGIN
	IF EXISTS (SELECT * 
			   FROM INSERTED I JOIN
									(SELECT MA_NVIEN, COUNT(*) AS SL
									FROM PHANCONG 
									GROUP BY MA_NVIEN) AS TMP ON I.MA_NVIEN = TMP.MA_NVIEN
			   WHERE SL >= 3
			   )
	BEGIN
		PRINT 'LOI: PHAN CONG KHONG HOP LE'
		ROLLBACK TRANSACTION
	END
	ELSE
	BEGIN
		PRINT 'THEM/SUA THANH CONG'
	END
END
GO

--7. Lương của trưởng phòng phải lớn hơn của tất cả nhân viên của phòng đó.
---			INSERT	DELETE	UPDATE
---NHANVIEN   +		  -		  +(LUONG)
---PHONGBAN   -		  -		  +(TRGPHG)
CREATE TRIGGER luongtrgphg_NV ON NHANVIEN
FOR INSERT, UPDATE
AS
BEGIN
	 IF EXISTS (SELECT *
				FROM INSERTED I JOIN PHONGBAN PB ON I.PHG = PB.MAPHG 
				WHERE I.LUONG > (SELECT LUONG 
								 FROM NHANVIEN NV
								 WHERE NV.MANV = PB.TRPHG)
				)
	BEGIN
		PRINT 'LOI: LUONG KHONG HOP LE'
		ROLLBACK TRANSACTION
	END
	ELSE
	BEGIN
		PRINT 'THEM/SUA THANH CONG'
	END
END
GO

CREATE TRIGGER luongtrgphg_PB ON PHONGBAN
FOR UPDATE
AS
BEGIN
	 IF EXISTS (SELECT *	
		FROM INSERTED I JOIN NHANVIEN NV ON I.TRPHG = NV.MANV
		WHERE LUONG < ANY (SELECT LUONG
						   FROM NHANVIEN NV
						   WHERE NV.PHG = I.MAPHG)
				)
	BEGIN
		PRINT 'LOI: LUONG KHONG HOP LE'
		ROLLBACK TRANSACTION
	END
	ELSE
	BEGIN
		PRINT 'THEM/SUA THANH CONG'
	END
END
GO

