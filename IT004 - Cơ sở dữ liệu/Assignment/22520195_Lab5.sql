--1. Số lượng nhân viên của mỗi bộ phận tối đa là 7 người.
---			INSERT	DELETE	UPDATE
---NHANVIEN   +        -       +(PHG)
CREATE TRIGGER slnv ON NHANVIEN
FOR INSERT, UPDATE
AS 
BEGIN
	IF (SELECT COUNT(*)
		FROM INSERTED INS JOIN PHONGBAN PB ON INS.PHG = PB.MAPHG
						  JOIN NHANVIEN NV ON NV.PHG = PB.MAPHG
		GROUP BY PB.MAPHG) > 8 
	BEGIN
		PRINT 'LOI: SO LUONG NHAN VIEN KHONG HOP LE'
		ROLLBACK TRANSACTION
	END
	ELSE
	BEGIN
		PRINT 'THEM/SUA THANH CONG'
	END
END
GO
	
--2. Lương của quản lý không được nhỏ hơn 25000
---			INSERT	DELETE	UPDATE
---NHANVIEN    +	  -		  +(LUONG)
CREATE TRIGGER luongnql ON NHANVIEN
FOR INSERT, UPDATE
AS 
BEGIN
	IF (SELECT LUONG
		FROM INSERTED
		WHERE MANV IN (
						SELECT MA_NQL
						FROM NHANVIEN
						)) < 25000
	BEGIN
		PRINT 'LOI: LUONG KHONG HOP LE'
		ROLLBACK TRANSACTION
	END
	ELSE
	BEGIN
		PRINT 'THEM/SUA THANH CONG'
	END
END
GO
--3. Một nhân viên chỉ được làm tối đa 3 dự án.
---			INSERT	DELETE	UPDATE
---PHANCONG    +	  -		  +(MADA)
CREATE TRIGGER slda ON PHANCONG
FOR INSERT, UPDATE
AS
BEGIN
	IF (SELECT COUNT(*)
		FROM PHANCONG
		GROUP BY MA_NVIEN
		) > 3
	BEGIN
		PRINT 'LOI: NHAN VIEN DUOC PHAN CONG TREN 3 DE AN'
		ROLLBACK TRANSACTION
	END
	ELSE
	BEGIN
		PRINT 'THEM/SUA THANH CONG'
	END
END
GO
--4. Ngày nhận chức của quản lý phải sau ngày sinh nhật của người đó.
---			INSERT	DELETE	UPDATE
---NHANVIEN    +	  -		  +(NGSINH, PHG)
---PHONGBAN    +	  -		  +(TRGPHG, NG_NHANCHUC)
CREATE TRIGGER ngnhanchuc_ngsinh_NV ON NHANVIEN
FOR INSERT, UPDATE
AS
BEGIN
	IF EXISTS(SELECT * 
			  FROM INSERTED INS JOIN PHONGBAN PB ON INS.MANV = PB.TRPHG
			  WHERE NGSINH > NG_NHANCHUC
			  )
	BEGIN
		PRINT 'LOI: NGAY SINH - NGAY NHAN CHUC KHONG HOP LE'
		ROLLBACK TRANSACTION
	END
	ELSE
	BEGIN
		PRINT 'THEM/SUA THANH CONG'
	END
END
GO

